// Comparision operator >, >=, <, <=,===, ==,!=,!==;

var ageTalha=11;
var ageAfnan=16;
console.log(ageTalha>ageAfnan);
console.log(ageTalha>=ageAfnan);
console.log(ageTalha<ageAfnan);
console.log(ageTalha<=ageAfnan);
console.log(ageTalha==ageAfnan);  // == loose equality
console.log(ageTalha===ageAfnan); // === strict equality
console.log(ageTalha!=ageAfnan); // != loose not equality or loose inequality 
console.log(ageTalha!==ageAfnan); // != strict not equality or strict inequality






